package io.richie.closures;

public class JavaOven {

    public static String bakeFruitPie(String fruit) {
        return "Baked into a delicious " + fruit + " pie!";
    }

    public static String bakeFruitTurnover(String fruit) {
        return "Baked into a delicious " + fruit + " turnover!";
    }

    public static String bakeFruitMuffin(String fruit) {
        return "Baked into a delicious " + fruit + " muffin!";
    }

}
